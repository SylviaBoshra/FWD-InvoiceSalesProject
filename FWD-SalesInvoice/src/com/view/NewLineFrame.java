    private void SaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBtnActionPerformed
        
        int InvNo;
        Double total;
        
        //Check if itemname or item price or item count is empty => error message will be displayed
        if(ItemNameTxt.getText().isEmpty() || ItemPriceTxt.getText().isEmpty() || ItemCountTxt.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "please make sure that you insert Item name, price, and count","Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        //Get the value selected from Invoice number combo box
        InvNo = Integer.parseInt((String)InvNoComboBox.getSelectedItem());
        //Get the header for the slected invoice number
        InvHeader header = c.getHeader(InvNo);
        //create new invoice line by the data inserted by the user in the text fields
        InvLine line = new InvLine (ItemNameTxt.getText() , Double.parseDouble(ItemPriceTxt.getText()) , Integer.parseInt(ItemCountTxt.getText()) ,header);
        //add the created line to the invoice lines array list
        c.addLine(line);
        //Display the new invoice line in the parent frame - invoice lines table
        frame.LoadInvLines(c.getInvLines());
        
        //Re calculate the total of invoice number
        total = c.GetHeaderTotal(InvNo);
        //Update the header total in the parent frame - header table
        frame.updateHeaderTotalTable(InvNo, total);
        
        //clear all fields
        ItemNameTxt.setText("");
        ItemPriceTxt.setText("");
        ItemCountTxt.setText("");
       
        
    }//GEN-LAST:event_SaveBtnActionPerformed

