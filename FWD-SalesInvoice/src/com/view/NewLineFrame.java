    //prevent the user from inserting any letters or special characters (only numbers)
    private void ItemPriceTxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ItemPriceTxtKeyPressed

            if ( (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') || (evt.getKeyChar() == KeyEvent.VK_BACK_SPACE)) {
               ItemPriceTxt.setEditable(true);
               priceLabel.setText("");
            } else {
               ItemPriceTxt.setEditable(false);
               priceLabel.setText("* Enter only numeric digits(0-9)");
            }
    }//GEN-LAST:event_ItemPriceTxtKeyPressed

